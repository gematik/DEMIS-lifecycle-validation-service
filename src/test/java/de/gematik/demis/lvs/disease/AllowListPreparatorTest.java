import static org.assertj.core.api.Assertions.assertThat;
import static org.junit.jupiter.api.Assertions.*;

import com.fasterxml.jackson.databind.ObjectMapper;
import java.io.IOException;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;

@ExtendWith(MockitoExtension.class)
class AllowListPreparatorTest {

  private final ObjectMapper objectMapper = new ObjectMapper();

  private AllowListPreparator allowListPreparator;

  @Test
  void shouldBuildAllowList() throws IOException {
    allowListPreparator =
        new AllowListPreparator("notifications/disease/diseaseConfiguration.json", objectMapper);

    AllowListPreparator result = allowListPreparator.build();

    assertThat(result.getAllowList()).isNotEmpty().hasSize(15);
  }
}
